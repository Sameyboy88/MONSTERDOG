def generate_fractal_map(size=32, scale=1.5):
    terrain = np.zeros((size, size))
    for x in range(size):
        for y in range(size):
            terrain[x][y] = np.sin(x / scale) * np.cos(y / scale) + random.uniform(-0.2, 0.2)
    return terrain

