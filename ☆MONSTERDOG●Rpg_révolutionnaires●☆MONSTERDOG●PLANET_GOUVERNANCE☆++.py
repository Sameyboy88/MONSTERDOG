        player = self.players[player_name]
        print(f"{player_name} engage un combat contre {enemy_type} ({enemy_hp} HP)!")
        
        while player["hp"] > 0 and enemy_hp > 0:
            attack = random.randint(10, 30)
            enemy_hp -= attack
            print(f"{player_name} attaque et inflige {attack} dégâts!")
            
            if enemy_hp <= 0:
                print(f"{enemy_type} est vaincu!")
                break
            
            enemy_attack = random.randint(10, 25)
            player["hp"] -= enemy_attack
            print(f"{enemy_type} riposte et inflige {enemy_attack} dégâts à {player_name}!")
            time.sleep(1)
        
        if player["hp"] <= 0:
            print(f"{player_name} a été vaincu...")

