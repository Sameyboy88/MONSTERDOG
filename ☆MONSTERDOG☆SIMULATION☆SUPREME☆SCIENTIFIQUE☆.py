    point_croisement = random.randint(1, len(parent1) - 1)
    enfant1 = np.concatenate((parent1[:point_croisement], parent2[point_croisement:]))
    enfant2 = np.concatenate((parent2[:point_croisement], parent1[point_croisement:]))
